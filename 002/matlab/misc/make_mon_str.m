function mon_str = make_mon_str(mon)

    if mon==1
        mon_str = 'January';
    elseif mon==2
        mon_str = 'February';
    elseif mon==3
        mon_str = 'March';
    elseif mon==4
        mon_str = 'April';
    elseif mon==5
        mon_str = 'May';
    elseif mon==6
        mon_str = 'June';
    elseif mon==7
        mon_str = 'July';
    elseif mon==8
        mon_str = 'August';
    elseif mon==9
        mon_str = 'September';
    elseif mon==10
        mon_str = 'October';
    elseif mon==11
        mon_str = 'November';
    elseif mon==12
        mon_str = 'December';
    end

end